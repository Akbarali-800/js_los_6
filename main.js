document.getElementById("priceButton").addEventListener("click", function () {
    let quantity = +prompt("Nechta Buyurtirasiz?");
    quantity = parseInt(quantity);

    switch (true) {
        case (quantity):
            alert("Iltimos, to'g'ri son kiriting.");
            break;
        default:
            let totalPrice = quantity * 45000;
            alert("Jami narx: " + totalPrice + " so'm");
            alert("Xaridingiz tayorlanmoqda")
            break;
    }
});

